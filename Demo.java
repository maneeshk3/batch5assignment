public class Demo {
 
    // Main Driver Method
    public static void main(String[] args)
    {
        int num = 54;                                            // Declaring and initializing integer variable
        
 
        if (num % 2 == 0) {                                    // Checking if number is even or odd number via remainder
         
                                                                 // If remainder is zero then this number is even
        System.out.println("Entered Number is Even");
        }    
            
        else {
 
        System.out.println("Entered Number is Odd");            // If remainder is not zero then this number is odd
 
        }    
    }      
            
} 